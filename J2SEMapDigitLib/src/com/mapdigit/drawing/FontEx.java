    /**
     * Return a shape array stored each glyph for characters in the char arrary.
     * @param  fontSize the size of the font.
     * @param  ac the array of characters.
     * @param  off the index of the first character to measure.
     * @param  len the number of characters to measure
     * @param  tdir the text direction.
     * @return a array of font character shapes.The origin of each shape is (0,0)
     */
    public IShape[] getGlyphArray(int fontSize, char[] ac, int off, int len,
            int tdir, AffineTransform transform) {
        if (this != FontEx.systemFont) {
            for (int i = off; i < off + len; i++) {
                loadGlyph(ac[i]);
            }
        }
        IShape[] shapes = new IShape[len];
        double scale = (double) fontSize / 256.0;
        int startIndex = off, step = 1, counter = 0;
        switch (tdir) {

            case TEXT_DIR_TB:
                startIndex = off;
                step = 1;
                break;
            case TEXT_DIR_RL:
                startIndex = off + len - 1;
                step = -1;
                break;
            default:
                startIndex = off;
                step = 1;
                break;
        }
        int i = startIndex;
        int offsetX = 0, offsetY = 0;
        while (counter < len) {
            Glyph glyph = (Glyph) glyphs.get(new Integer(ac[i]));
            if (glyph != null) {
                AffineTransform matrix = new AffineTransform();
                matrix.scale(scale, -scale);
                matrix.translate(0, -200);
                matrix.translate(offsetX, offsetY);

                if (tdir == TEXT_DIR_TB) {
                    offsetX = 0;
                    offsetY -= 256;
                } else {
                    offsetX += glyph.width;
                    offsetY = 0;
                }
                if (transform != null) {
                    matrix.preConcatenate(transform);
                }
                IShape shape = matrix.createTransformedShape(glyph.path);

                shapes[counter] = shape;
            }
            counter++;
            i += step;
        }

        return shapes;
    }

