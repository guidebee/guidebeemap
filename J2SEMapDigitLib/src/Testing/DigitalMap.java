    private void drawCursor(IGraphics g)  {
        int x = mapLabel.getWidth() / 2;
            int y = mapLabel.getHeight() / 2;
        g.setColor(0x205020);
                g.drawRect(x - 4, y - 4, 8, 8);
                g.drawLine(x, y - 6, x, y - 2);
                g.drawLine(x, y + 6, x, y + 2);
                g.drawLine(x - 6, y, x - 2, y);
                g.drawLine(x + 6, y, x + 2, y);
    }
